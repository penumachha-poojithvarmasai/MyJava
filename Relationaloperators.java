package myjava;

public class Relationaloperators {
	public static void main(String args[]) {
		int x=5;
		int y =2;
		
		System.out.println(x<y);//false
		System.out.println(x<=y);//false
		System.out.println(x>y);//true
		System.out.println(x>=y);//true
		System.out.println(x==y);//false
		System.out.println(x!=y);//true
		
		
	
	}

}
